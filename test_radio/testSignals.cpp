void setup() {
  Serial.begin(115200);
  delay(5000);

  if (radio.begin(CC1101::MOD_ASK_OOK, 433.92, 4.0) != CC1101::STATUS_OK) {
    Serial.println("CC1101 init failed!");
    while (1);
  }

  radio.setSyncMode(CC1101::SYNC_MODE_NO_PREAMBLE);
  radio.setCrc(false);
  radio.setAddressFilteringMode(CC1101::ADDR_FILTER_MODE_NONE);
  radio.setPacketLengthMode(CC1101::PKT_LEN_MODE_FIXED, sizeof(codeA));

  Serial.println("Sending gate code...");

  // vysle sekvenci dvakrat
  for (int i = 0; i < 2; i++) {
    CC1101::Status s = radio.transmit(codeA, sizeof(codeA));
    if (s != CC1101::STATUS_OK) {
      Serial.print("Transmit failed: ");
      Serial.println(s);
    } else {
      Serial.print("Sent ");
      Serial.println(i + 1);
    }
    delay(10);
  }

  Serial.println("Done.");
}

